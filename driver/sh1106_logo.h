static inline void sh1106_boot_draw_char(u8 *fb, int x, int y, char c)
{
    int col, idx;
    const u8 *glyph;

    if (c < ' ' || c > 'Y')
        return;

    glyph = boot_font[c - ' '];
    idx   = (y / 8) * 128 + x;

    for (col = 0; col < 5; col++) {
        if (x + col >= 128)
            break;
        fb[idx + col] |= glyph[col];
    }
}

static inline void sh1106_boot_draw_text(u8 *fb, int x, int y, const char *str)
{
    while (*str) {
        sh1106_boot_draw_char(fb, x, y, *str++);
        x += 6;
        if (x >= 128)
            break;
    }
}

/*
 * sh1106_boot_build() - construye el framebuffer del boot screen
 * @fb: puntero a u8[1024], el framebuffer del driver
 *
 * Solo rellena el buffer. El driver lo vuelca por I2C
 * con su propia sh1106_update_display().
 */
static inline void sh1106_boot_build(u8 *fb)
{
    int page, col;

    memset(fb, 0, 1024);

    /* Logo izquierda */
    for (page = 0; page < LOGO_H / 8; page++)
        for (col = 0; col < LOGO_W; col++)
            fb[page * 128 + LOGO_X + col] =
                logo_bitmap[page * LOGO_W + col];

    /* Texto derecha */
    sh1106_boot_draw_text(fb, BOOT_TEXT_X,  8, "BOOTING");
    sh1106_boot_draw_text(fb, BOOT_TEXT_X, 24, "KERNEL");
    sh1106_boot_draw_text(fb, BOOT_TEXT_X, 32, "LOADED");
    sh1106_boot_draw_text(fb, BOOT_TEXT_X, 48, "SYSTEM");
    sh1106_boot_draw_text(fb, BOOT_TEXT_X, 56, "INIT...");
}

